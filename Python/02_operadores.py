### Operadores ###

# Los operadores nos permiten hacer cuentas, son los simbolos de suma resta division etc

print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
# Se utilizan los dos asteriscos para hacer potencias. por jemplo 3 ** 4 seria (3*3*3*3)
print(3 ** 4)
# el simbolo de porcentaje se llama diferencial, lo que te muetra es el resto que nos queda luego de una division
print(10 % 3)

# el simbolo + puede unir diferentes variables en tanto y en cuanto sean del mismo tipo
print("hola " + "pytohn")

# divisoines con dos barras (//) te da el resultado sin tener en cuenta el resto( redondea el resultado)
print(10 // 3)

### Operadores comparativos ###

print(3 > 4)
print(3 < 4)
print(3 <= 4)
print(3 >= 4)